class ListItem {
  final String title;
  final String category;
  final String location;
  final double rating;
  final int reviews;
  final String price;
  final String imageUrl;

  ListItem({
    required this.title,
    required this.category,
    required this.location,
    required this.rating,
    required this.reviews,
    required this.price,
    required this.imageUrl,
  });
}
